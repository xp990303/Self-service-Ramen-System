package seventeen.group.Entity;

/**
 * @className: Customer
 * @author: Group 17
 * @date: 2020/5/22
 * @description: The type Customer.
 */
public class Customer {
    private String uid;
    private String firname;
    private String surname;
    private String email;
    private String mobile;
    private int stamp;
    private String msgChk;
    private String judgeCode;

    /**
     * Gets uid.
     *
     * @return the uid
     */
    public String getUid() {
        return uid;
    }

    /**
     * Sets uid.
     *
     * @param uid the uid
     */
    public void setUid(String uid) {
        this.uid = uid;
    }

    /**
     * Gets firname.
     *
     * @return the firname
     */
    public String getFirname() {
        return firname;
    }

    /**
     * Sets firname.
     *
     * @param firname the firname
     */
    public void setFirname(String firname) {
        this.firname = firname;
    }

    /**
     * Gets surname.
     *
     * @return the surname
     */
    public String getSurname() {
        return surname;
    }

    /**
     * Sets surname.
     *
     * @param surname the surname
     */
    public void setSurname(String surname) {
        this.surname = surname;
    }

    /**
     * Gets email.
     *
     * @return the email
     */
    public String getEmail() {
        return email;
    }

    /**
     * Sets email.
     *
     * @param email the email
     */
    public void setEmail(String email) {
        this.email = email;
    }

    /**
     * Gets mobile.
     *
     * @return the mobile
     */
    public String getMobile() {
        return mobile;
    }

    /**
     * Sets mobile.
     *
     * @param mobile the mobile
     */
    public void setMobile(String mobile) {
        this.mobile = mobile;
    }

    /**
     * Gets stamp.
     *
     * @return the stamp
     */
    public int getStamp() {
        return stamp;
    }

    /**
     * Sets stamp.
     *
     * @param stamp the stamp
     */
    public void setStamp(int stamp) {
        this.stamp = stamp;
    }

    /**
     * Gets msg chk.
     *
     * @return the msg chk
     */
    public String getMsgChk() {
        return msgChk;
    }

    /**
     * Sets msg chk.
     *
     * @param msgChk the msg chk
     */
    public void setMsgChk(String msgChk) {
        this.msgChk = msgChk;
    }

    /**
     * Gets judge code.
     *
     * @return the judge code
     */
    public String getJudgeCode() {
        return judgeCode;
    }

    /**
     * Sets judge code.
     *
     * @param judgeCode the judge code
     */
    public void setJudgeCode(String judgeCode) {
        this.judgeCode = judgeCode;
    }
}
